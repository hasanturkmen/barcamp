			<h2>Featured Speakers</h2>
			<p>Although this conference encourages everyone to participate by speaking or helping out during an event, we have some exciting confirmed speakers. Here's a short list of who'll be at the event. Register for the event if you're interested on speaking at the event.</p>

			<h3>Photography: The Anatomy of Creative Inspiration</h3>
			<h4> with Kelly and Courtney Canova</h4>
			<img class="img-rounded" src="images/speaker_kellyandcourtneycanova.jpg" />
			<p>Kelly and Courtney will show you how to assemble and transform a comprehensive expression of art into a marketable business proposition. Learn from the pros how to carefully select and position your image through photo shoots, print ads and more as they talk about the anatomy of executing an idea, breaking down the elements of photography and maintaining communication with clients.</p> 

			<p>Kelly and Courtney Canova are award-winning artists that specialize in artistic wedding photography and graphic design. Both with degrees in art and design fields from The Ringling School of Art and the Art Institute of Ft. Lauderdale, they have a passion for their work, relationships with clients and communication through visual means.</p>

			<h3>Typography: Evolution & Trends From Print To Web Design</h3>
			<h4>with Martha Garzon</h4>
			<img class="img-rounded" src="images/speaker_marthagarzon.jpg" />
			<p>Since the invention of the printing press in 1400's, typography has evolved to become one of the most important tools in visual communications. This lecture explores type both in historical and contemporary contexts. Discover & get inspired by emerging typographic trends in print & web design.</p>

			<p>Martha Garzon is a creative director, educator & web/graphic designer. After studying visual arts at the National University in Bogota, she moved to the US where she acquired a B.A. in Digital Arts from Stetson University. She has been teaching multimedia, web development, photography and design since 2004 and currently teaches at the Digital Arts Program at Stetson University and is the founder/owner of M5 Design Studio.</p>

			<h3>3D: No glasses required</h3>
			<h4>with Michael Kappers</h4>
			<img class="img-rounded" src="images/speaker_michaelkappers.jpg" />

			<p>Michael Kappers, the Program Manager of Digital Media at Seminole State College, will give a talk and demonstration about basic modeling techniques in a 3D environment. Michael will also introduce the creation and application of shaders and materials. In addition he will demonstrate the animation process and examine how objects move in real life and emulate the movement in CG.</p>
			
			<p>Michael holds a Master of Fine Arts degree from the University of Central Florida. His undergraduate degree is in Fine Arts with a concentration in Graphic Design from George Mason University. Prior to teaching, Michael cut his teeth in the world of 3D graphics by developing Computer Based simulation and Training for the military. He also worked in the gaming industry and was on the team that developed titles including Duke Nukem Time to Kill and Die Hard Trilogy.</p>

			<h3>Design: Process, Practices, and Style</h3>
			<h4> with Clark Orr</h4>
			<img class="img-rounded" src="images/speaker_clarkorr.jpg" />
			<p>Clark Orr, a native DeLand, FL resident is a freelance graphic designer and illustrator whose focus is primarily in branding, print, packaging, and apparel design. Clark currently splits his time as a Head Designer for the Boston-based t-shirt brand Johnny Cupcakes and working as a contracted designer for brands such as: Nike, The Hundreds, Benny Gold, Rebel 8, & several others.</p>

			<p>Clark has given lectures on his story, design theory, and business practice to UCF, Stetson University, and Daytona State College. At Bar Camp DeLand, Clark will be giving a presentation on The Design Process, Practices, and Style.</p>

			<h3>Music: Traditional Music goes Interactive</h3>
			<h4>with Matt Roberts</h4>
			<img class="img-rounded" src="images/speaker_mattroberts.jpg" />
			<p>Matt Roberts will give a talk and demonstration on how to turn traditional musical instruments into interactive devices to control the playback and manipulation of video and audio in real-time.</p>

			<p>Matt Roberts is a new media artist specializing in real-time video performance and new media applications. His work has been featured internationally and nationally, including shows in Brazil, Canada, Argentina, Italy, Mexico and in New York, San Francisco, Miami, and Chicago. He has shown in several new media festivals including ISEA, FILE, 404, CONFLUX, and he recently received the Transitio award from the Transitio_MX Festival in Mexico City. He is the founder of MPG: Mobile Performance Group , and an Associate Professor of Digital Art at Stetson University.</p>

			<h3>Front End: Bootstrap &amp; CSS Preprocessors</h3>
			<h4>with Ray Villalobos</h4>
			<img class="img-rounded" src="images/speaker_rayvillalobos.jpg" />
			<p>As responsive design continues to take over the web, front-end developers and designers have turned to preprocessors and layout systems that simplify their workflow. Lynda.com staff author Ray Villalobos will talk about using the Bootstrap framework from Twitter to scaffold and fast track your responsive design. He'll talk about how you can use CodeKit and LESS to put together designs in hours instead of days.</p>
			
			<p>Ray Villalobos is a full-time author and teacher at lynda.com. He is author of the book, Exploring Multimedia for Designers. He has more than 20 years experience in developing and programming multimedia projects. Previously at Entravision Communications, he designed and developed a network of radio station and TV web sites. As a senior producer for Tribune Interactive, he was responsible for designing orlandosentinel.com and for creating immersive multimedia projects and Flash games for the site.</p>

			<h3>Development: From Python to Ruby</h3>
			<h4>with Gordon Worley</h4>
			<img class="img-rounded" src="images/speaker_gordonworley.jpg" />
			<p>I started out as a Python programmer. In the intervening years I've done lots of different things, but Python was for a long time my "native" language. But for the past year and a half I've been programming Ruby and it has quickly replaced Python as my language of choice. In this talk I'll describe why I like Ruby so much and how my various programming experiences have led me to like Ruby so much.</p>
			
