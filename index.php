			<h2>What is BarCamp Deland?</h2>
			<p>BarCamp Deland is ad-hoc unconference event centered around technology. It aims to bring people together from different backgrounds like web-design, photography, graphic design web-development and technology for cooperative learning. Born from the desire for people to share and learn in an open environment. It is an intense event with discussions, demos and interaction from attendees. Anyone with something to contribute or with the desire to learn is welcome and invited to join. When you come, be prepared to share.</p>
	
	
			<h2>No spectators...only participants</h2>
			<p>Attendees must give a demo, a session, or help with one, or otherwise volunteer/contribute in some way to support the event. All presentations are scheduled the day they happen. Prepare in advance, but come early to get a slot on the wall. The people present at the event will select the demos or presentations they want to see. Presenters are responsible for making sure that notes/slides/audio/video of their presentations are published on the web for the benefit of all and those who can’t be present.</p>

			<h2>Rules of Deland BarCamp</h2>
			<ul>
				<li>If you want to present, you must write your topic and name in a presentation slot.</li>
				<li>As many presentations at a time as facilities allow for.</li>
				<li>No pre-scheduled presentations</li>
				<li>Presentations will go on as long as they have to or until they run into another presentation slot.</li>
			</ul>

