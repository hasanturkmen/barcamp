<h3>Development: From Python to Ruby</h3>
<h4> with Gordon Worley</h4>
<img class="img-rounded" src="images/speaker_gordonworley.jpg" />
<p>I started out as a Python programmer. In the intervening years I've done lots of different things, but Python was for a long time my "native" language. But for the past year and a half I've been programming Ruby and it has quickly replaced Python as my language of choice. In this talk I'll describe why I like Ruby so much and how my various programming experiences have led me to like Ruby so much.</p>

